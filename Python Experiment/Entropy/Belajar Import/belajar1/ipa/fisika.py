def gaya(m, a):
    '''rumus menghitung gaya  
    F = m.a  
    Keterangan:  
    F = gaya  
    m = massa  
    a = percepatan'''
    
    F = m * a
    return F

def berat(m, g):
    '''rumus menghitung berat  
    w = m.g  
    Keterangan:  
    w = berat  
    m = massa  
    g = percepatan gravitasi'''
    
    w = m * g
    return w