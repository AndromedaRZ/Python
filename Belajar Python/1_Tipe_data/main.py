# Tipe data dalam python

# tipe  data: Angka satuan (integer)
data_integer = 10
print("angka satuan:", data_integer)
print("bertipe:",type(data_integer))

# tipe data: Angka berkoma (float)
data_float = 10.5
print("angka berkoma:", data_float)
print("bertipe:",type(data_float))

# tipe data: Kumpulan karakter/Teks (string)
data_string = "Hello, World!"
print("kumpulan karakter:", data_string)
print("bertipe:",type(data_string))

# tipe data: Boolean atau biner (True/False)
data_boolean = True
print("boolean atau biner:", data_boolean)
print("bertipe:",type(data_boolean))

# tipe data khusus 

# tipe data kompleks (complex)
data_complex = 3 + 4j
print("bilangan kompleks:", data_complex)
print("bertipe:",type(data_complex))

# tipe data dari bahasa pemograman lain 

# tipe data dari bahasa c
from ctypes import c_int
data_c_int = c_int(10)
print("tipe data dari bahasa C:", data_c_int)
print("bertipe:",type(data_c_int))


